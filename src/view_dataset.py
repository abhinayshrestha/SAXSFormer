#!/usr/bin/env python3
"""
view_dataset.py

Utility script for inspecting and validating the serialized SAXS dataset
generated by the preprocessing pipeline.

Purpose
-------
This script loads the compressed NumPy dataset archive:

    data/processed/saxs_dataset.npz

and performs basic validation and inspection of the stored arrays.
It helps verify that the dataset was generated correctly before it is
used for machine learning training or analysis.

Dataset Structure
-----------------
The .npz archive contains the following arrays:

    x          → SAXS intensity curves (shape: N × M)
                 where N = number of proteins
                       M = number of q-sampling points

    q          → q-values (momentum transfer) corresponding to each
                 SAXS intensity measurement

    y_rg       → Radius of Gyration (Rg) extracted from CRYSOL

    y_dmax     → Maximum particle dimension (Dmax) extracted from CRYSOL

    y_volume   → Excluded volume of the protein

    ids        → PDB identifiers corresponding to each protein sample

Typical Example
---------------
For a large dataset the shapes may look like:

    Curves (x): (100000, 512)
    q values:   (512,)
    Rg:         (100000,)
    Dmax:       (100000,)
    Volume:     (100000,)
    IDs:        (100000,)

Output
------
The script prints:

- dataset structure and shapes
- basic data integrity statistics
- the first 10 protein records including:
      Protein ID
      Radius of Gyration
      Maximum Dimension
      Excluded Volume

Dependencies
------------
- NumPy
- Pandas

Usage
-----
Run directly from the project root:

    python view_dataset.py
"""

import numpy as np
import pandas as pd


def load_and_display(file_path="data/processed/saxs_dataset.npz"):
    # Load the compressed numpy file
    with np.load(file_path, allow_pickle=True) as data:
        ids = data["ids"]
        rg = data["y_rg"]
        dmax = data["y_dmax"]
        curves = data["x"]
        q = data["q"]
        volume = data["y_volume"]
    print("\n--- DATASET STRUCTURE ---")
    print("Keys:", ["ids", "x", "q", "y_rg", "y_dmax"])

    print("\nShapes:")
    print("Curves (x):", curves.shape)
    print("q values:", q.shape)
    print("Rg:", rg.shape)
    print("Dmax:", dmax.shape)
    print("IDs:", ids.shape)

    # Dataset health checks
    print("\n--- DATASET HEALTH CHECK ---")
    print("NaN in curves:", np.isnan(curves).sum())
    print("NaN in q:", np.isnan(q).sum())
    print("NaN in Rg:", np.isnan(rg).sum())
    print("NaN in Dmax:", np.isnan(dmax).sum())

    print("Inf in curves:", np.isinf(curves).sum())

    # Verify q matches curve length
    curve_length = curves.shape[1]
    q_length = len(q) if q.ndim == 1 else q.shape[1]

    print("\nCurve length:", curve_length)
    print("q length:", q_length)

    if curve_length != q_length:
        print("WARNING: q length does not match curve length!")
    # Create a Pandas DataFrame for easy viewing
    df = pd.DataFrame(
        {
            "Protein_ID": ids,
            "Radius_of_Gyration": rg,
            "Max_Dimension": dmax,
            "Excluded_Volume": volume,
            "Curve_Vector": [
                list(c) for c in curves
            ],  # Store the 512 points as a list in the cell
        }
    )

    # Display the first few rows
    print(f"Dataset Loaded: {len(df)} rows")
    print("-" * 30)

    # We use .head() to show the top rows
    # and adjust pandas options to see all columns
    pd.set_option("display.max_columns", None)
    pd.set_option("display.width", 1000)

    print(
        df[
            ["Protein_ID", "Radius_of_Gyration", "Max_Dimension", "Excluded_Volume"]
        ].head(20)
    )

    return df


def main():
    df = load_and_display()


if __name__ == "__main__":
    main()
